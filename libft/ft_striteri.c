#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	size_t	i;

	if (s && f)
	{
		i = 0;
		while (i < (size_t)ft_strlen(s))
		{
			f(i, &s[i]);
			i++;
		}
	}
}
